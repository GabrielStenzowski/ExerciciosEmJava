import java.util.Scanner;

public class program {
	public static void main(String[] args) {
		
		Scanner leitor = new Scanner(System.in);
		
		int repeticao = 5;
		
		int numero[] = new int[repeticao];
		int dobro[] = new int[repeticao];
		
		for (int i = 0; i < numero.length; i++) {
			System.out.println("Fala o numero " + (i + 1) + ":");		
			numero[i] = leitor.nextInt();
			dobro[i] = numero[i] * 2;
		}
		
		for (int i = 0; i < dobro.length; i++) {
			System.out.println("Esse é o dobro dos numeros " + (i+1) + ":");
			System.out.println(dobro[i]);
		}
		
		
		leitor.close();
	
	}
	
	
}
